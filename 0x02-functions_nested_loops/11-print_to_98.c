#include "main.h"

/**
 * print_to_98 - Check Holberton
 * @n: A input integer
 * Description: function that prints all natural numbers
 * from n to 98, followed by a new line.
 * Return: Nothing
 */

void print_to_98(int n)
{
	int x;

	for (x = n; x < n; x++)
	{
		_putchar('$x');
		_putchar(',');
		_putchar(' ');
	}
	putchar('98');
	return ;
}
